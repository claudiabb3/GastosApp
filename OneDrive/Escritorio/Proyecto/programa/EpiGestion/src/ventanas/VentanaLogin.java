  // metodo par avalidar el usuario y contrase単a
    public void validar(){
    
        String nombre = txUsuario.getText();
        String pass = txPass.getText();
        
        //En caso de que los campos esten varcios, avisamos con un dialog
        if(txUsuario.getText().isEmpty() || txPass.getText().isEmpty()){
        JOptionPane.showMessageDialog(this, "Tienes que introducir usuario y contrase単a");
       
        }else{
        
            //Para poder comprobar los datos del usuario de la bd, llamamos al metodo valirdarUsuario de la 
            //clase login.
            usuario = log.varlidarUsuario(nombre, pass);
            
            
            //En caso de qe los datos del usuario existan en la bd, se accede a la ventana de inicio
            if(usuario.getNombre() != null && usuario.getPass() != null){
                Inicio ventanaInicio = new Inicio();
                ventanaInicio.setVisible(true);
                dispose();
             
            }else{
           
             //En caso de que algun datos sea incorrecto, se avisa con un dialog
                if(usuario.getNombre() ==null){
                    JOptionPane.showMessageDialog(this, "nombre error");}
                else if(usuario.getPass() ==null)
                    {
                    JOptionPane.showMessageDialog(this, "contrase単a error");}
                    
                        txUsuario.requestFocus();
            }
        
        }
    }
    
